package com.string;

public class PrettyPrinting {
    public static void main(String[] args) {
        float num=12.345f;
        System.out.printf("Formatted number is %.2f\n",num);
        System.out.println("Formatted number is "+num);
        System.out.printf("Pie: %.3f\n", Math.PI);
        System.out.printf("Hello my name is %s and I am %s\n","roshan","cool");
    }
}
